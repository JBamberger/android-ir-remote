    private void printCarrierFrequencies() {
        StringBuilder stringBuilder = new StringBuilder();
        ConsumerIrManager.CarrierFrequencyRange[] carrierFrequencies = irManager.getCarrierFrequencies();
        int j = carrierFrequencies.length;
        int i = 0;
        while (i < j) {
            ConsumerIrManager.CarrierFrequencyRange freqRange = carrierFrequencies[i];
            int k = freqRange.getMinFrequency();
            int m = freqRange.getMaxFrequency();
            stringBuilder.append(k);
            if (m != k) {
                stringBuilder.append(" - ").append(m);
            }
            stringBuilder.append(", ");
            i += 1;
        }
        Log.d("IrService", "Supported frequencies: " + stringBuilder);
    }

